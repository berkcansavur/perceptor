const LANG_KEY = "perceptorLang";

// localStorage exists in the webview but not in the test/node runtime; guard both
// the read at module load and the write in setLang so importing i18n stays safe.
function storedLang(): LangId {
  if (typeof localStorage === "undefined") {
    return "en";
  }
  const saved = localStorage.getItem(LANG_KEY) as LangId;
  return I18N[saved] ? saved : "en";
}

let currentLang: LangId = storedLang();
let langChangeHandler: (() => void) | null = null;

export type TVars = Record<string, string | number>;

export function t(key: string, vars?: TVars): string {
  const table = I18N[currentLang] ?? I18N.en;
  let translation = table[key] ?? I18N.en[key] ?? key;
  if (vars) {
    for (const name of Object.keys(vars)) {
      translation = translation.replace(new RegExp(`\\{${name}\\}`, "g"), String(vars[name]));
    }
  }
  return translation;
}

export function getCurrentLang(): LangId {
  return currentLang;
}

export function applyI18n(): void {
  for (const element of document.querySelectorAll<HTMLElement>("[data-i18n]")) {
    const key = element.dataset["i18n"];
    if (key) {
      element.innerHTML = t(key);
    }
  }
  for (const element of document.querySelectorAll<HTMLInputElement>("[data-i18n-placeholder]")) {
    const key = element.dataset["i18nPlaceholder"];
    if (key) {
      element.placeholder = t(key);
    }
  }
}

export function onLangChange(handler: () => void): void {
  langChangeHandler = handler;
}

export function setLang(lang: string): void {
  currentLang = lang === "tr" ? "tr" : "en";
  if (typeof localStorage !== "undefined") {
    localStorage.setItem(LANG_KEY, currentLang);
  }
  const select = document.getElementById("lang-select") as HTMLSelectElement | null;
  if (select) {
    select.value = currentLang;
  }
  applyI18n();
  if (langChangeHandler) {
    langChangeHandler();
  }
}
